        <br><br>1. 申請條件
        <br> 全程就讀同高中且沒有轉學的應屆畢業生
        <br> 符合各校系訂定的學測及在校成績百分比門檻
        <br> 在校百分比至少要在 50％ 內
        <br>2. 參加規則（繁星介紹篇會有更詳細的繁星規則喔）
        <br> （註記：底線處附上專欄文章連結）
