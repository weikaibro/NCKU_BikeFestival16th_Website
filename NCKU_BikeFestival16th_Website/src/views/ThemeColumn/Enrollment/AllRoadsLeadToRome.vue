        <br><br> 申請條件
        <br> 報考學測：要參加個人申請一定要報考學測，每個科系參考的科目不同，可依照你想要申請的科系去選考。國、英要考到高三上的內容，數A/B、自、社則是考必修的內容。108課綱著重素養導向，因此每一科都會有非選題和大量閱讀題組，大家要記得練習閱讀速度喔～

